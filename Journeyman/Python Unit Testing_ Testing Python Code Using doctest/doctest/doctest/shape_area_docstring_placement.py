"""
>>> triangle(6, 7)
21.0
"""
def triangle(height, base):
    """
    >>> triangle(4, 3)
    6.0
    """
    return 0.5 * height * base


def rectangle(length, breadth):
    """
    >>> rectangle(4, 3)
    12
    """
    return length * breadth


def square(side):
    """
    >>> square(5)
    25
    """
    return side**2